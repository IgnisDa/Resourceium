#!/usr/bin/env bash

PASSWORD="${PASSWORD}"

MAIN_DB="${DATABASE_NAME}"

mysql -u root -p<<MYSQL_SCRIPT
CREATE DATABASE $MAIN_DB;

CREATE DATABASE ${MAIN_DB} /*\!40100 DEFAULT CHARACTER SET utf8 */;
CREATE USER ${MAIN_DB}@localhost IDENTIFIED BY '${PASSWORD}';
GRANT ALL PRIVILEGES ON ${MAIN_DB}.* TO '${MAIN_DB}'@'localhost';
FLUSH PRIVILEGES;
MYSQL_SCRIPT
